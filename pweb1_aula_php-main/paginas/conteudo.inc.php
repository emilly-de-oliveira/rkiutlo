<h1>Os Vingadores 2 - Filmagens do longa estão concluídas</h1>
<p>As filmagens de Os Vingadores 2 - A Era de Ultron estão concluídas. A informação foi
revelada pelo diretor do longa, Joss Whedon, no Twitter. "Vingadores 2 está concluído. O que
eu faço agora?", tuitou Whedon.</p>
<p>Durante o painel da Marvel na Comic-Con, Josh Brolin foi confirmado como Thanos. Os
Vingadores 2 - A Era de Ultron chega aos cinemas em maio de 2015.</p>
